public class IfElse {
    public static void main(String []args){
        int zahl = 3; 
        if(zahl == 1){
            System.out.println("Hugo");
        }else if(zahl == 2){
            System.out.println("Lisa");
        }else if(zahl == 3){
            System.out.println("Anna");
        }else if(zahl == 4){
            System.out.println("Emma");
        }else{
            System.out.println("Egon");
        } 
    }
}
