/**
 * Kreisberechnung
 */
public class Kreis {
    public static void main(String[]args){
        float pi, radius, umfang;
        pi = 3.14159F;
        radius = 2.5F;
        umfang = 2 * pi * radius;
        System.out.println("Radius = " + radius);
        System.out.println("Umfang = " + umfang);
    }
}
